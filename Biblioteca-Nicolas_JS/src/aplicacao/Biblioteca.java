  private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    Livro livro = new Livro(banco.getConexao());
    livro.setAnoLancamento(Integer.parseInt(txtAnoLancamento.getText()));
    livro.setNumeroPaginas(Integer.parseInt(txtNumeroPaginas.getText()));
    livro.setIdioma((String) cbIdioma.getSelectedItem());
    livro.setPaisOrigem((String) cbPaisOrigem.getSelectedItem());
    livro.setTitulo(txtTitulo.getText());
    livro.setResumo(txtResumo.getText());
    try{
      if(lblIdLivro.getText().equals("")){
      //Novo livro
        livro.incluiLivro();
        lblIdLivro.setText(Integer.toString(livro.getIdLivro()));
        JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!","Informação",JOptionPane.INFORMATION_MESSAGE);
      }
      else{
        //Atualiza livro
        livro.setIdLivro(Integer.parseInt(lblIdLivro.getText()));
        livro.atualizaLivro(Integer.parseInt(lblIdLivro.getText()));
        JOptionPane.showMessageDialog(this, "Registro alterado com sucesso!","Informação",JOptionPane.INFORMATION_MESSAGE);
      }
      carregaListaLivros();
    }
    catch(SQLException e){
      JOptionPane.showMessageDialog(this, "Ocorreu um erro ao salvar o livro","Erro",JOptionPane.ERROR_MESSAGE);
    }
      
   
  }//GEN-LAST:event_btnSalvarActionPerformed

  private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
      Livro livro = new Livro(banco.getConexao());
      try {
          if (lblIdLivro.getText().equals("")) {
              JOptionPane.showMessageDialog(this, "Não há nenhum registro selecionado.", "Erro", JOptionPane.ERROR_MESSAGE);
          } else {
              //Atualiza livro
              livro.setIdLivro(Integer.parseInt(lblIdLivro.getText()));
              livro.deletaLivro(Integer.parseInt(lblIdLivro.getText()));
              JOptionPane.showMessageDialog(this, "Registro deletado com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
              lblIdLivro.setText("");
              txtTitulo.setText("");
              txtAnoLancamento.setText("");
              txtNumeroPaginas.setText("");
              txtResumo.setText("");
              cbIdioma.setSelectedIndex(0);
              cbPaisOrigem.setSelectedIndex(0);
          }
          carregaListaLivros();
      } catch (SQLException e) {
          JOptionPane.showMessageDialog(this, "Ocorreu um erro ao deletar o livro", "Erro", JOptionPane.ERROR_MESSAGE);
      }
  }//GEN-LAST:event_btnDeletarActionPerformed

