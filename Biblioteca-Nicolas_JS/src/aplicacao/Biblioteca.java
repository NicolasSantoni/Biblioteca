  public void carregaListaLivros(){
    Livro livro = new Livro(banco.getConexao());
    try{
      listaLivros = livro.obterListaLivros();
    }
    catch(SQLException e){
      JOptionPane.showMessageDialog(this, "Erro ao obter a lista de livros.","Ocorreu um erro",JOptionPane.ERROR_MESSAGE);
    }
  }
  
  public void atualizaCampos(){
    try {
      lblIdLivro.setText(Integer.toString(listaLivros.getInt("idLivro")));
      txtTitulo.setText(listaLivros.getString("titulo"));
      txtAnoLancamento.setText(Integer.toString(listaLivros.getInt("anoLancamento")));
      txtNumeroPaginas.setText(Integer.toString(listaLivros.getInt("numeroPaginas")));
      cbIdioma.setSelectedItem(listaLivros.getString("idioma"));
      cbPaisOrigem.setSelectedItem(listaLivros.getString("paisOrigem"));
      txtResumo.setText(listaLivros.getString("resumo"));
    } 
    catch (SQLException ex) {
     JOptionPane.showMessageDialog(this, "Erro ao definir os valores nos campos.","Ocorreu um erro",JOptionPane.ERROR_MESSAGE);
    }
    
  }

